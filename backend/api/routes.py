# Setup Upload Directory
BASE_DIR = Path(__file__).resolve().parent.parent
UPLOAD_DIR = BASE_DIR / "uploads"
os.makedirs(UPLOAD_DIR, exist_ok=True)

async def run_workflow_background(session_id: str, job_id: str, file_path: str, file_name: str, user_request: str):
    async with AsyncSessionLocal() as db:
        try:
            # 1. Update job status to 'running'
            await jobs.update_job_status(db, job_id, "running")
            await db.commit()
            
            # 2. Build initial state
            input_artifact = FileArtifact(
                artifact_id=f"file_{session_id}",
                name=file_name,
                path=Path(file_path),
                source="upload"
            )
            
            initial_state = {
                "session_id": session_id,
                "user_request": user_request,
                "workflow_status": "idle",
                "input_files": [input_artifact],
                "output_files": [],
                "agent_queue": [],
                "agent_results": {},
                "shared_data": {},
                "workbook_insights": [],
                "errors": [],
                "messages": []
            }
            
            # 3. Invoke LangGraph workflow
            final_state = await workflow_app.ainvoke(initial_state)
            
            # Save assistant messages generated by graph to DB
            for msg in final_state.get("messages", []):
                if msg["role"] == "assistant":
                    await crud.create_message(
                        db,
                        session_id,
                        role="assistant",
                        content=msg["content"]
                    )
            
            errors = final_state.get("errors", [])
            if errors:
                await jobs.update_job_status(db, job_id, "failed", errors=errors)
            else:
                output_files = []
                for artifact in final_state.get("output_files", []):
                    # We expose the file name relative to settings.EXPORT_DIR or downloads
                    output_files.append({
                        "name": artifact.name,
                        "path": str(artifact.path)
                    })
                    
                result_payload = {
                    "final_summary": final_state.get("final_summary", ""),
                    "output_files": output_files
                }
                await jobs.update_job_status(db, job_id, "completed", result=result_payload)
                
            await db.commit()
        except asyncio.CancelledError:
            # Task was cancelled
            pass
        except Exception as e:
            await jobs.update_job_status(db, job_id, "failed", errors=[str(e)])
            await db.commit()
        finally:
            jobs.active_tasks.pop(job_id, None)

@router.post("/upload")
async def upload_file(
    file: UploadFile = File(...),
    db: AsyncSession = Depends(get_db)
):
    ext = file.filename.split(".")[-1].lower()
    if ext not in settings.get_allowed_extensions_list():
        raise HTTPException(status_code=400, detail="Unsupported file type.")
    
    session_id = str(uuid.uuid4())
    file_path = UPLOAD_DIR / f"{session_id}.{ext}"
    
    # Save file to disk
    with open(file_path, "wb") as f:
        content = await file.read()
        f.write(content)
        
    import shutil
    os.makedirs(settings.EXPORT_DIR, exist_ok=True)
    report_filename = f"{session_id}_report.xlsx"
    report_path = Path(settings.EXPORT_DIR) / report_filename
    if ext in ["xlsx", "xls"]:
        shutil.copy(file_path, report_path)
    else:
        # If it's a CSV, convert it to Excel so openpyxl can append sheets later
        import pandas as pd
        df = pd.read_csv(file_path)
        df.to_excel(report_path, index=False)
        
    db_session = await crud.create_session(db, file.filename, str(file_path), session_id=session_id)
    await crud.update_session_report_path(db, session_id, str(report_path))
    
    # Try profiling to extract initial metadata
    try:
        import pandas as pd
        if ext == "csv":
            df = pd.read_csv(file_path)
        else:
            df = pd.read_excel(file_path)
        metadata = get_dataframe_metadata(df)
    except Exception as e:
        metadata = {"error": f"Failed to extract metadata: {e}"}
        
    await crud.create_message(
        db,
        session_id,
        "assistant",
        f"Uploaded {file.filename}. Ready for analysis!",
        metadata={"df_metadata": metadata}
    )
    await db.commit()
    
    return {
        "session_id": session_id,
        "file_name": file.filename,
        "metadata": metadata
    }

@router.post("/analyze/async")
async def start_analysis(
    session_id: str,
    user_request: str,
    db: AsyncSession = Depends(get_db)
):
    session = await crud.get_session(db, session_id)
    if not session:
        raise HTTPException(status_code=404, detail="Session not found")
        
    job_id = str(uuid.uuid4())
    
    # Save user message to chat history
    await crud.create_message(db, session_id, "user", user_request)
    
    # Create database job tracking record
    await jobs.create_job(db, session_id, job_id, user_request)
    await db.commit()
    
    # Spawn background task
    task = asyncio.create_task(
        run_workflow_background(
            session_id=session_id,
            job_id=job_id,
            file_path=session.file_path,
            file_name=session.file_name,
            user_request=user_request
        )
    )
    jobs.active_tasks[job_id] = task
    
    return {
        "job_id": job_id,
        "status": "pending"
    }

