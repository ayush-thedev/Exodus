from fastapi.responses import FileResponse

@router.get("/export/{session_id}")
async def export_report(session_id: str, db: AsyncSession = Depends(get_db)):
    session = await crud.get_session(db, session_id)
    if not session or not getattr(session, "report_path", None) or not os.path.exists(session.report_path):
        raise HTTPException(status_code=404, detail="Export report not found")
        
    return FileResponse(
        path=session.report_path,
        media_type="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
        filename=f"exodus_report_{session_id}.xlsx"
    )
