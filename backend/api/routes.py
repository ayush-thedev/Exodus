@router.post("/analyze/cancel/{job_id}")
async def cancel_analysis(
    job_id: str,
    db: AsyncSession = Depends(get_db)
):
    job = await jobs.get_job(db, job_id)
    if not job:
        raise HTTPException(status_code=404, detail="Job not found")
        
    cancelled_job = await jobs.cancel_job(db, job_id)
    await db.commit()
    
    return {
        "job_id": job_id,
        "status": cancelled_job.status if cancelled_job else "cancelled"
    }

@router.post("/chat/{session_id}")
async def chat(
    session_id: str,
    message: str,
    db: AsyncSession = Depends(get_db)
):
    session = await crud.get_session(db, session_id)
    if not session:
        raise HTTPException(status_code=404, detail="Session not found")
        
    # Get history
    history_models = await crud.get_messages(db, session_id)
    history = [{"role": m.role, "content": m.content} for m in history_models]
    
    # Save user message to database
    await crud.create_message(db, session_id, "user", message)
    history.append({"role": "user", "content": message})
    await db.commit()
    
    # Profile workbook
    input_artifact = FileArtifact(
        artifact_id=f"file_{session_id}",
        name=session.file_name,
        path=Path(session.file_path),
        source="upload"
    )
    
    temp_state = {
        "session_id": session_id,
        "report_path": getattr(session, "report_path", None),
        "input_files": [input_artifact],
        "messages": history,
        "follow_up_request": message,
        "errors": []
    }
    
    explorer = DataExplorerAgent()
    temp_state = explorer.process(temp_state)
    
    # Process with FollowUp Agent
    follow_up = FollowUpAgent()
    final_state = follow_up.process(temp_state)
    
    result = final_state.get("follow_up_result")
    
    response_content = "Failed to compile follow-up response."
    metadata = {}
    if result:
        response_content = result.summary
        metadata = {
            "result_kind": result.result_kind,
            "result_value": result.result_value,
            "generated_code": result.generated_code,
            "error": result.error,
            "fig_base64": result.fig_base64
        }
        
    await crud.create_message(
        db,
        session_id,
        "assistant",
        response_content,
        msg_type="code" if (result and result.generated_code) else "text",
        metadata=metadata
    )
    await db.commit()
    
    return {
        "role": "assistant",
        "content": response_content,
        "msg_type": "code" if (result and result.generated_code) else "text",
        "metadata": metadata
    }

